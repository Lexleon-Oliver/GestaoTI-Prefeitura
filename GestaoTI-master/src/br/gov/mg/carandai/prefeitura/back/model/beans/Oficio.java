
package br.gov.mg.carandai.prefeitura.back.model.beans;


public class Oficio {
    private String numOfic;
    private String anoOfic;
    private String data;
    private String de;
    private String para;
    private String assunto;
    private int id;

    public String getNumOfic() {
        return numOfic;
    }

    public void setNumOfic(String numOfic) {
        this.numOfic = numOfic;
    }

    public String getAnoOfic() {
        return anoOfic;
    }

    public void setAnoOfic(String anoOfic) {
        this.anoOfic = anoOfic;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getDe() {
        return de;
    }

    public void setDe(String de) {
        this.de = de;
    }

    public String getPara() {
        return para;
    }

    public void setPara(String para) {
        this.para = para;
    }

    public String getAssunto() {
        return assunto;
    }

    public void setAssunto(String assunto) {
        this.assunto = assunto;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
