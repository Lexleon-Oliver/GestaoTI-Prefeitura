
package br.gov.mg.carandai.prefeitura.back.model.beans;


public class Memorando {
    private String numMemo;
    private String anoMemo;
    private String data;
    private String de;
    private String para;
    private String assunto;
    private int id;

    public String getNumMemo() {
        return numMemo;
    }

    public void setNumMemo(String numMemo) {
        this.numMemo = numMemo;
    }

    public String getAnoMemo() {
        return anoMemo;
    }

    public void setAnoMemo(String anoMemo) {
        this.anoMemo = anoMemo;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getDe() {
        return de;
    }

    public void setDe(String de) {
        this.de = de;
    }

    public String getPara() {
        return para;
    }

    public void setPara(String para) {
        this.para = para;
    }

    public String getAssunto() {
        return assunto;
    }

    public void setAssunto(String assunto) {
        this.assunto = assunto;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
