
package br.gov.mg.carandai.prefeitura.back.model.beans;


public class Rims {
    private String numRims;
    private String anoRims;
    private String numProc;
    private String anoProc;
    private String ficha;
    private String fonte;
    private String solicit;
    private String fornec;
    private String dataPed;
    private int id;
   
    private String prod;
    private boolean entregue;

    public String getNumRims() {
        return numRims;
    }

    public void setNumRims(String numRims) {
        this.numRims = numRims;
    }

    public String getAnoRims() {
        return anoRims;
    }

    public void setAnoRims(String anoRims) {
        this.anoRims = anoRims;
    }

    public String getNumProc() {
        return numProc;
    }

    public void setNumProc(String numProc) {
        this.numProc = numProc;
    }

    public String getAnoProc() {
        return anoProc;
    }

    public void setAnoProc(String anoProc) {
        this.anoProc = anoProc;
    }

    public String getFicha() {
        return ficha;
    }

    public void setFicha(String ficha) {
        this.ficha = ficha;
    }

    public String getFonte() {
        return fonte;
    }

    public void setFonte(String fonte) {
        this.fonte = fonte;
    }

    public String getSolicit() {
        return solicit;
    }

    public void setSolicit(String solicit) {
        this.solicit = solicit;
    }

    public String getFornec() {
        return fornec;
    }

    public void setFornec(String fornec) {
        this.fornec = fornec;
    }

    public String getDataPed() {
        return dataPed;
    }

    public void setDataPed(String dataPed) {
        this.dataPed = dataPed;
    }

    public String getProd() {
        return prod;
    }

    public void setProd(String prod) {
        this.prod = prod;
    }

    public boolean isEntregue() {
        return entregue;
    }

    public void setEntregue(boolean entregue) {
        this.entregue = entregue;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    
    
    
}
